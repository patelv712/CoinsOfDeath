public final class Guy {
    private double width;
    private double height;
    private double x;
    private double y;

    public double getWidth() {
        return this.width;
    }

    public double getHeight() {
        return this.height;
    }
    
    public double getX() {
        return this.x;
    }
    
    public double getY() {
        return this.y;
    }
}
